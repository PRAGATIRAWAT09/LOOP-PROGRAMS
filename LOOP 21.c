#include <stdio.h>
void main() 
{
int firstTerm, commonDifference, numberOfTerms; // Print arithmetic progression in series.
int currentTerm;
printf("Enter the first term of the A.P. series: ");
scanf("%d", &firstTerm);
printf("Enter the common difference of the A.P. series: ");
scanf("%d", &commonDifference);
printf("Enter the number of terms to print: ");
scanf("%d", &numberOfTerms);
printf("The A.P. series is: ");
currentTerm = firstTerm;
for (int i = 0; i < numberOfTerms; i++) 
{
    printf("%d ", currentTerm);
    currentTerm += commonDifference; // Calculate the next term
}
printf("\n"); 
getch();
}
