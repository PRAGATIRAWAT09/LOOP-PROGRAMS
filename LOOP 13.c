#include <stdio.h>
void main() 
{
int num, rem, product = 1; // find product of digits of a number.
printf("Enter a number: ");
scanf("%d", &num);
while (num != 0) 
{
     rem = num % 10;      
     product = product * rem;       
     num = num/10;            
}
printf("The product of digits is: %d\n", product);
getch();
}
