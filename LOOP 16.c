#include <stdio.h>
void main() 
{
int num, i, sum = 0; // check perfect number.
printf("Enter a number: ");
scanf("%d", &num);
for (i = 1; i < num; i++) 
{
    if (num % i == 0) 
       sum = sum + i;
}
if (sum == num) 
  printf("%d is a perfect number.\n", num);
else
  printf("%d is not a perfect number.\n", num);
getch();
}
