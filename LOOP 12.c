#include <stdio.h>
void main() 
{
int num, sum = 0, remainder; // find the sum of digits of a number.
printf("Enter a number: ");
scanf("%d", &num);
while (num!= 0) 
{
     remainder = num % 10;
     sum = sum+ remainder;      
     num=num/10;            
}
printf("Sum of digits: %d\n", sum);
getch();
}
