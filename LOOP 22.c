#include <stdio.h>
void main() // Print geometric progression series.
{
int a; // First term  
int r; // Common ratio
int n; // Number of terms
int current_term; // Variable to store the current term 
printf("Enter the first term of the G.P. series: ");
scanf("%d", &a);
printf("Enter the common ratio of the G.P. series: ");
scanf("%d", &r);
printf("Enter the total number of terms: ");
scanf("%d", &n);
current_term = a;
printf("The Geometric Progression Series is: ");
for (int i = 0; i < n; i++) 
{
    printf("%d ", current_term);
    current_term *= r; // Calculate the next term
}
getch();
}
